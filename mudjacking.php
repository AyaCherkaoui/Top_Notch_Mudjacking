      <section class="page-content page-content-alt">
        <h2 class="section-heading">What should I seal?</h2>
        <p class="section-text">
          You should seal any open expansion joint, control joint or random
          crack that is wide enough to take on water.
        </p>
        <div class="seal-types-grid">
          <div class="seal-type">
            <h3>Expansion Joints</h3>
            <p>
              Expansion Joints keep concrete from buckling under the pressure of
              expansion. Once in a while you will hear about highways heaving up
              due to the expansion of concrete in extreme heat. Common areas of
              expansion are where concrete meet sections of other areas. Such
              areas include next to foundations, garages, sidewalks, curb or
              driveway addition (Old concrete to new).
            </p>
          </div>
          <div class="seal-type">
            <h3>Control Joints</h3>
            <p>
              Control Joints are not to be confused with expansion joints. They
              serve two different purposes. Control joints are grooved joints
              tooled in while finishing the concrete. More common in the present
              they are saw cut in green concrete usually within 24 hours of the
              concrete being poured and finished. As the name implies the
              purpose of the control joint is to control where the concrete slab
              cracks. AND IT WILL CRACK!
            </p>
          </div>
          <div class="seal-type">
            <h3>Random Cracks</h3>
            <p>
              These are cracks that occur in areas other than the control
              joints. Some are caused by the tooled/saw cut on the control joint
              that were not made deep enough at the control joint. Concrete
              cracks at the weakest point so the deeper the cut the more control
              over the crack. Random cracks can also come from bad or soft sub
              base causing cracking from slab movement. Heavy loads can also
              cause random cracks.
            </p>
          </div>
        </div>
      </section>

      <section class="page-content">
        <h2 class="section-heading">Concrete Facts</h2>
        <div class="about-text">
          <p>
            Concrete is made up with water as the water evaporates you get
            shrinkage cracks. These cracks should happen in control joints. A
            slab will continue to shrink and widen over time for years, however,
            most of the shrinkage takes place within the first year and
            especially within the first 90 days. This is why architects specify
            a maximum of 80 sq ft per slab between control joints.
          </p>
          <p>
            Concrete slabs with the proper slope for water will gather a lot of
            water rushing towards the control joints. If not properly sealed
            water will penetrate the joints washing out the sub base. One sq
            yard of concrete is equal to 5 gallons of water at an inch of rain
            fall. A standard slab of concrete at 10' x 8' (80 sq ft) is 8.89 sq
            yards. This equals 44.45 gallons of water (Almost 9 five gallon
            buckets) rushing towards an open joint.
          </p>
          <p class="concrete-quote">
            There are three rules to concrete &ndash; It will get hard, it will
            crack, and nobody can steal it!!
          </p>
