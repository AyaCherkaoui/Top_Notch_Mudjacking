            <h2>Crack Joint Sealing/Caulking</h2>
            <h3>Why I should seal my concrete joints?</h3>
            <p>
              There are many reasons for sealing joints and open cracks in your
              concrete slabs. Concrete joint sealants will prolong the life of
              your concrete and minimize repairs. Same reason you clean and wax
              your vehicles... PREVENTIVE MAINTENANCE.
            </p>
            <p>
              Unsealed joints invite moisture migrating through joints to the
              sub base causing erosion. This causes the ground to settle beneath
              the slabs and over time can heave and sink or even crack. In the
              winter water under your slabs will freeze and cause frost heave
              (Less water, less heaving). Sealing expansion joints at garage and
              house keeps salt from running down along the foundation as salt
              eats away the foundation. Big plus, it also sheds water away from
              your foundation for a drier basement.
            </p>
            <p>
              Caulking will also prevent debris and weeds from gathering in
              joints during the expansion and contraction of the concrete caused
              by weather cycles. Small debris, such as rocks and pebbles, get
              into the joints impacting adjacent slab sections. Slab distress
              can result into rocking slabs and even vertical displacement at
              the joints.
            </p>
          </div>
