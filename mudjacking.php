        <h2 class="section-heading">How to choose the right concrete lifting method. Here’s a “side by side by side” comparison</h2>
        <p class="section-text">Mudjacking vs Foam Jacking vs Tear Out and Replace</p>
        <p class="section-text">
          When comparing the three options to fix your settled concrete here are
          a couple of differences that will stand out.
        </p>
        <div class="comparison-grid">
          <div class="comparison-card">
            <h3>Mudjacking - Traditional</h3>
            <p>
              Involves drilling slightly larger holes and using a cement-based
              material, which consists of blending Portland cement, sand and
              water. This “mud mixture” fills hidden voids, displaces water, and
              gently lifts the slab back into place. It offers strong
              environmental resistance, excellent structural support, and a lower
              overall cost.
            </p>
            <p>
              <strong>Limitations:</strong> The only limitation is the slightly larger holes,
              which can be somewhat visible but always patched to blend into your
              concrete as best as possible.
            </p>
            <p>
              <strong>Advantages:</strong> Cost effective, high strength material stabilizes the
              base soil, all natural, non-decaying material which allows the
              ground to breathe, and is a time – tested solution.
            </p>
          </div>
          <div class="comparison-card">
            <h3>Foam (Poly) Jacking</h3>
            <p>
              Uses a two-part liquid material (engineered foam chemical), that
              combines during injection to create an expanding polyurethane foam.
              Poly (foam) can typically only raise concrete 2” – 4”. Anything
              more than that would require multiple lifts or other options. Poly
              is good for small lifts, but in our area, most require larger lifts
              that possibly have voids under them. Poly wouldn’t be a cost effect
              approach but the holes that they drill will be slightly smaller.
            </p>
            <p>
              Ground squirrels love chewing at this to make their winter bedding.
            </p>
          </div>
          <div class="comparison-card">
            <h3>Tear out and Replace</h3>
            <p>
              Tearing out and replacing your concrete can typically take 2-5
              days. First the company will come out and tear out the concrete and
              haul it away. Then they form up the job, lay gravel as a base and
              then pour the concrete. After the concrete has cured, they will come
              back and tear out the forms. Then the new concrete can be used
              (walked on or parked on). Disruptive to your landscaping.
            </p>
          </div>
